# Variables - are containers to store a data values.
# python has no command to declare a variable. 
# variable is created the moment you assign a value to it.
# variables do not need to be declared with a particular data type, they can be changed after they've been set.

"""
Example:
x = 4       # x is of type int
x = "Sally" # x is now of type str
print(x)
"""

# Casting - is used if we want to declare our variable

"""
Example:
x = str(3)    # x will be '3'
y = int(3)    # y will be 3
z = float(3)  # z will be 3.0
"""

# we can get the type of a variable by using the "type()" function

"""
Example:
x = 5
y = "John"
print(type(x))
print(type(y))
"""

